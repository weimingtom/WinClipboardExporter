package com.iteye.weimingtom.wce.model;

//attendee
public class ContactInfo {
	private int id;
	private int contactId; //序号
	private String session; //届次
	private String postTimeBegin; //任职时间开始
	private String postTimeEnd; //任职时间结束
	private String number; //编号
	private String name; //姓名
	private String sex; //性别
	private String job; //职务
	private String catalog; //组别
	private String photo; //照片
	private String cardId; //卡号
	private String phone; //手机
	private String fax; //传真
	private String email; //电子邮箱
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getContactId() {
		return contactId;
	}
	public void setContactId(int contactId) {
		this.contactId = contactId;
	}
	public String getSession() {
		return session;
	}
	public void setSession(String session) {
		this.session = session;
	}
	public String getPostTimeBegin() {
		return postTimeBegin;
	}
	public void setPostTimeBegin(String postTimeBegin) {
		this.postTimeBegin = postTimeBegin;
	}
	public String getPostTimeEnd() {
		return postTimeEnd;
	}
	public void setPostTimeEnd(String postTimeEnd) {
		this.postTimeEnd = postTimeEnd;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public String getCatalog() {
		return catalog;
	}
	public void setCatalog(String catalog) {
		this.catalog = catalog;
	}
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	public String getCardId() {
		return cardId;
	}
	public void setCardId(String cardId) {
		this.cardId = cardId;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getFax() {
		return fax;
	}
	public void setFax(String fax) {
		this.fax = fax;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	
}
